CREATE VIEW VW_RELATORIO
AS
SELECT PRO.NOME AS 'PRODUTO', O.DATA, U.NOME AS 'USUARIO', PRO.PRECO, OI.QUANTIDADE,
        (PRO.PRECO * OI.QUANTIDADE) AS 'TOTAL'
FROM ORDEM_ITENS OI
INNER JOIN PRODUTO PRO ON PRO.ID = OI.PRODUTO_ID
INNER JOIN ORDEM O ON O.ID = OI.ORDER_ID
INNER JOIN USUARIO U ON U.ID = O.USER_ID


SELECT USUARIO, SUM(TOTAL)
FROM VW_RELATORIO
GROUP BY USUARIO;