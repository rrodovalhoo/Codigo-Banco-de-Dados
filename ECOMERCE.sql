CREATE DATABASE ECOMERCE;

USE ECOMERCE;

CREATE TABLE CATEGORIA(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    DESCRICAO VARCHAR(1000) NULL,
    PARENT_CATEGORIA_ID INT NULL
);

CREATE TABLE PRODUTO(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    CATEGORIA_ID INT NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    DESCRICAO VARCHAR(1000) NULL,
    PRECO NUMERIC(9,2) NULL,
    STOCK VARCHAR(100) NULL,
    SKU INT NULL,
    IMAGEM_URL VARCHAR(100) NULL,
    CONSTRAINT FK_CATEGORIA_PRODUTO foreign key(CATEGORIA_ID)
    REFERENCES CATEGORIA(ID)
);

CREATE TABLE USUARIO(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL UNIQUE,
    SENHA VARCHAR(100) NOT NULL UNIQUE,
    CREATED_AT DATETIME NULL DEFAULT NOW(),
    TELEFONE VARCHAR(50) NULL
);

CREATE TABLE ENDERECO(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    USER_ID INT NOT NULL,
    RUA VARCHAR(100) NOT NULL,
    CIDADE VARCHAR(100) NOT NULL,
    ESTADO VARCHAR(50) NOT NULL,
    CEP VARCHAR(50) NULL,
    TIPO INT NULL,
    PADRAO VARCHAR(100) NULL DEFAULT 'sim',
    CONSTRAINT FK_ENDERECO_USUARIO FOREIGN KEY(USER_ID)
    REFERENCES USUARIO(ID)
);

CREATE TABLE CARRINHO(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    USER_ID INT NOT NULL,
    CREATED_AT DATETIME DEFAULT NOW(),
    UPDATED_AT DATETIME DEFAULT NOW(),
    CONSTRAINT FK_CARRINHO_USUARIO FOREIGN KEY(USER_ID)
    REFERENCES USUARIO(ID)
);

CREATE TABLE ORDEM(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    USER_ID INT NOT NULL,
    DATA DATETIME DEFAULT NOW(),
    TOTAL NUMERIC(9,2) NULL,
    STATUS VARCHAR(50) DEFAULT 'LIVRE',
    CONSTRAINT FK_USUARIO_ORDEM FOREIGN KEY(USER_ID)
    REFERENCES USUARIO(ID)
);

CREATE TABLE ORDEM_ITENS(
    ID INT NOT NULL auto_increment PRIMARY KEY,
    ORDER_ID INT NOT NULL,
    PRODUTO_ID INT NOT NULL,
    QUANTIDADE SMALLINT NULL DEFAULT 1,
    PRECO NUMERIC(9,2) NULL,
    CONSTRAINT FK_ORDEM_OI FOREIGN KEY(ORDER_ID)
    REFERENCES ORDEM(ID),
    CONSTRAINT FK_PRODUTO_OI FOREIGN KEY(PRODUTO_ID)
    REFERENCES PRODUTO(ID)
);


